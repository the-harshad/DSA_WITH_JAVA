package Array;
import java.util.*;

public class Maxidiftwoelementsarray {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr = new int[n];
        for (int i = 0; i < n; ++i) {
            arr[i] = sc.nextInt();
        }
        ArrayProblem(arr);
        sc.close();
    }

    public static void ArrayProblem(int[] arr) {
        int min = arr[0];
        int max = arr[0];

        for (int i = 1; i < arr.length; i++) {
            if (arr[i] < min) {
                min = arr[i];
            }
            if (arr[i] > max) {
                max = arr[i];
            }
        }

        int maxDifference = max - min;
        System.out.println(maxDifference);
    }
}
