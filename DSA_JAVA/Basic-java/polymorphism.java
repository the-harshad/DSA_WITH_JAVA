public class polymorphism {
// polymorphism 
    public static void print (String s){
        System.out.println(s); }
    
    public static void print(int num){
        System.out.println("call now--");
        System.out.println(num); }
    public static void main(String[] args) {
        print("hello");
        print(5261);
        print("hyyy"); }
}